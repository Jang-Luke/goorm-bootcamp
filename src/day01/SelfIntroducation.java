        System.out.println(delimiter);
        System.out.println("                 자기소개                 ");
        System.out.println(delimiter);
        System.out.println("   이름: 장길웅");
        System.out.println("   나이: 32세");
        System.out.println("   취미: 코딩");
        System.out.println("   목표: Java 마스터 되기!");
